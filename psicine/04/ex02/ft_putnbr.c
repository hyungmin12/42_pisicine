#include<unistd.h>

void	rev(int nb)
{
	char	ch;

	if (nb == 0)
		return ;
	else
	{
		rev(nb / 10);
		ch = '0' + (nb % 10);
		write(1, &ch, 1);
	}
}

void	ft_putnbr(int nb)
{
	char	c;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb *= -1;
		}
		rev(nb / 10);
		c = nb % 10 + '0';
		write(1, &c, 1);
	}
}
